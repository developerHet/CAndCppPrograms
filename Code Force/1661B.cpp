int MOD=1<<15;
 
 
 
void solve() {
    int n;
    cin >> n;
    int ans = 15;
    for(int i=0;i<15;i++) {
        for(int j=0;j<15;j++) {
            if(((n+i)<<j)%MOD==0) {
                ans=min(ans,i+j);
            }
        }
    }
    cout << ans << endl;
}
 
 
 
